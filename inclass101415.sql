FROM CUSTOMER C INNER JOIN ORDERS O ON C.CUSTOMER_NUM=O.CUSTOMER_NUM
	 INNER JOIN ORDER_LINE L ON O.ORDER_NUM=L.ORDER_NUM INNER JOIN
	 ITEM I ON L.ITEM_NUM=I.ITEM_NUM
WHERE I.DESCRIPTION='ROCKING HORSE'

