	  ( 
	  SELECT REP_NUM
	  FROM CUSTOMER
	  WHERE CREDIT_LIMIT >= 10000 
	  )


--10
SELECT *
FROM REP R
WHERE EXISTS
	  ( 
	  SELECT C.REP_NUM
	  FROM CUSTOMER C
	  WHERE CREDIT_LIMIT >= 10000 AND R.REP_NUM=C.REP_NUM
	  )

--WITHOUT SUBQUERY
